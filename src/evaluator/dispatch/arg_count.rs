/// Checks if the argument count is valid for a known built-in function.
/// If invalid, emits the appropriate Wolfram-style error message and returns
/// the unevaluated expression. If valid or unknown, returns None.
pub fn check_arg_count(
  name: &str,
  args: &[Expr],
) -> Option<Result<Expr, InterpreterError>> {
  let (min, max) = get_arg_count_range(name)?;
  let n = args.len();

  if n >= min && (max == usize::MAX || n <= max) {
    return None; // arg count is valid
  }

  // Format the error message following Wolfram Language conventions:
  // - argx: exactly 1 argument expected
  // - argrx: exactly N (>1) arguments expected
  // - argt: min or max arguments expected (max - min == 1)
  // - argb: between min and max arguments are expected (max - min > 1)
  let arg_word = if n == 1 { "argument" } else { "arguments" };

  let msg = if min == max {
    if min == 1 {
      format!(
        "{}::argx: {} called with {} {}; 1 argument is expected.",
        name, name, n, arg_word
      )
    } else {
      format!(
        "{}::argrx: {} called with {} {}; {} arguments are expected.",
        name, name, n, arg_word, min
      )
    }
  } else if max == usize::MAX {
    // Variadic with minimum — shouldn't normally reach here since we only
    // check when n < min (max is unlimited)
    format!(
      "{}::argm: {} called with {} {}; {} or more arguments are expected.",
      name, name, n, arg_word, min
    )
  } else if max - min == 1 {
    format!(
      "{}::argt: {} called with {} {}; {} or {} arguments are expected.",
      name, name, n, arg_word, min, max
    )
  } else {
    format!(
      "{}::argb: {} called with {} {}; between {} and {} arguments are expected.",
      name, name, n, arg_word, min, max
    )
  };

  crate::emit_message(&msg);

  Some(Ok(Expr::FunctionCall {
    name: name.to_string(),
    args: args.to_vec(),
  }))
}
